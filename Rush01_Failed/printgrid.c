#include <unistd.h>

void	ft_putnbr(int nb);

void	print_grid(int **grid)
{
	int	i;
	int	j;
	
	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			ft_putnbr(grid[i][j]);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
